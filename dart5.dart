// Arithmetic operations in Dart
void main() {
  int a = 10;
  int b = 5;

  // Addition
  int sum = a + b;
  print("Sum: $sum");

  //  Subtraction
  int difference = a - b;
  print("Difference: $difference");

  //Division
  double quotient = a / b;
  print("Quotient: $quotient");

  // modulus
  int remainder = a % b;
  print("Remaider: $remainder");

  print("---------------------");

  int m = 10;
  print("++m = ${++m}");
  print("m++ = ${m++}");
  print("--m = ${--m}");
  print("m-- = ${m--}");
}
